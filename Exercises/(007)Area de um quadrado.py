print("Esse programa irá receber dados sobre um quadrado, em seguida calculará sua área e o drobro dela.")
base = int(input("Me dê a base desse quadrado: "))
area = base * base
print("Essa é a área do quadrado: {}cm²".format(area))
dobro = area*2
print("Já o dobro desta área é de: {}cm²".format(dobro))